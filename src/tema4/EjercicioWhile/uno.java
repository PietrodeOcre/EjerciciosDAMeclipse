    public static void main(String[] args) {
        // TDefinir variables
        int numero;
        numero = 1; //Asignamos valor
        while (numero<=10){ //Evaluamos la condición
            System.out.println(numero); //Mostramos el número
            numero++; //Aumentamos el número para que haga de contador
        }
    }
    
