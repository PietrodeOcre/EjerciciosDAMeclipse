public class dos {
    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        int numero; // Definir variables
        numero = 100; //Asignamos valor
        while (numero>=1) { //Evaluamos condición
            System.out.println(numero); //Mostramos número
            numero--; //Restamos para conseguir el contador
            
        }
    }
}
